package Tema2Practicando;

public class BuclesEJ {
    public static void main(String[] args) {

        int contador = 0;

        while (contador <= 10){
            System.out.println(contador);
            contador++;
        }

        //Saca un bucle los numeros del -10 al 10
        contador = -10;

        while (contador <= 10){
            System.out.println(contador);
            contador++;
        }

        //Contador del 10 al 1
        contador = 10;

        while (contador >= 1){
            System.out.println(contador);
            contador--;
        }




        System.out.println("Fin del programa");



    }
}
