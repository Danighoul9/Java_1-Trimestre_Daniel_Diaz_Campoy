package Tema2Practicando;

public class BucleDoWHILE {
    public static void main(String[] args) {

        //DO-WHILE: igual que el while pero se ejecuta al menos una vez el contador
        //Numeros del 1-10

        int contador = 1;

        do{
            System.out.println(contador);
            contador++;
        }while(contador <= 10);

        contador = 1;
        while(contador <= 10){
            System.out.println(contador);
            contador++;
        }







    }
}
